#include <stdio.h>
/**
 * main - Entry point
 *
 * Description: 'the program's description'
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	char lowercase_alphabet;

	for (lowercase_alphabet = 'a'; lowercase_alphabet <= 'z'; ++lowercase_alphabet)
		putchar(lowercase_alphabet);

	return (0);
}
